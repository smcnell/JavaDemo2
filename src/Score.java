public class Score {
    private int mathScore;
    private int readingScore;
    private int alphabetScore;
    private int countingScore;
    private boolean followsDirections;

    public int getMathScore() {
        return mathScore;
    }

    public void setMathScore(int mathScore) {
        this.mathScore = mathScore;
    }

    public int getReadingScore() {
        return readingScore;
    }

    public void setReadingScore(int readingScore) {
        this.readingScore = readingScore;
    }

    public int getAlphabetScore() {
        return alphabetScore;
    }

    public void setAlphabetScore(int alphabetScore) {
        this.alphabetScore = alphabetScore;
    }

    public int getCountingScore() {
        return countingScore;
    }

    public void setCountingScore(int countingScore) {
        this.countingScore = countingScore;
    }

    public boolean isFollowsDirections() {
        return followsDirections;
    }

    public void setFollowsDirections(boolean followsDirections) {
        this.followsDirections = followsDirections;
    }
}
